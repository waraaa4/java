package day04_20211007;

public class Ex01_For {

	public static void main(String[] args) {

		for (int i = 1; i <= 3; i++) {
			// 1 2 3
			System.out.println(i);
		}
		

		for (int i = 1; i < 3; i++) {
			// 1 2
			System.out.println(i);
		}

		for (int i = 5; i >= 1; i--) {
			// 5 4 3 2 1
			System.out.println(i);
		}

		for (int i = 1; i <= 10; i += 2) {
			// 1 3 5 7 9
			System.out.println(i);
		}
		
		
		
	}

}
