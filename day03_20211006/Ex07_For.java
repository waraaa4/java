package day03_20211006;

public class Ex07_For {

	public static void main(String[] args) {
		
		/*
		 * 1부터 5까지 각각 다른줄에 출력하는 코드를 작성해 보세요.
		 */
		
		int num = 0;
		num = 1;
		System.out.println(num);
		num = 2;
		System.out.println(num);
		num = 3;
		System.out.println(num);
		num = 4;
		System.out.println(num);
		num = 5;
		System.out.println(num);
		
		//for문을 이용하여 1부터 3까지 출력
		for(int i=1; i<=3; i++) {
			System.out.println(i);
		}
		
		
		
		
	}

}
